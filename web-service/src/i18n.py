DEFAULT_LANGUAGE = "en"


def t(key: str, lang: str = DEFAULT_LANGUAGE) -> str:
    entry = T.get(key)
    if entry is None:
        return key
    return entry.get(lang) or entry.get(DEFAULT_LANGUAGE) or key


def t_fmt(key: str, lang: str = DEFAULT_LANGUAGE, **kwargs) -> str:
    s = t(key, lang)
    for k, v in kwargs.items():
        s = s.replace("{" + k + "}", str(v))
    return s
